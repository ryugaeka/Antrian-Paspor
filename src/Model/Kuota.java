package Model;

/**
 *
 * @author Reynold
 */
public class Kuota implements Interface{
    private String bulan;
    private int kuota;

    public Kuota(String bulan, int kuota) {
        this.bulan = bulan;
        this.kuota = kuota;
    }

    public String getBulan() {
        return bulan;
    }

    public void setBulan(String bulan) {
        this.bulan = bulan;
    }

    public int getKuota() {
        return kuota;
    }

    public void setKuota(int kuota) {
        this.kuota = kuota;
    }

}

